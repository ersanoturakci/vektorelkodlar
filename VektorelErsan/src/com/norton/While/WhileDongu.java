package com.norton.While;
import java.util.Scanner;

public class WhileDongu {

	
	public static void main(String[] args) {
		
		int deger=0;
		
		Scanner sayi=new Scanner (System.in);
		deger = sayi.nextInt();
				
		while (deger < 5){
			System.out.println("dön1");
			System.out.println("dön2");
			System.out.println("dön3");
		
			deger ++;
		}
		
		
		
		
	}
}
